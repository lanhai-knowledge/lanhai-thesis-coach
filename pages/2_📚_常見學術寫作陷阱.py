st.markdown(
    "嵐海顧問團隊整理碩博士生在文獻撰寫時最常踩到的五個雷區。"
    "這些不是格式潔癖，而是會直接被審稿人盯上的硬傷——"
    "看完後請拿一段你已寫好的文獻探討，逐項自我檢查。"
)
st.markdown("---")

# === 陷阱 1 ===
st.markdown("### 陷阱 1 ｜ AI 工具捏造引用")
st.warning(
    "**症狀**：你問 ChatGPT 或 Claude「請給我 5 篇 XXX 的研究」，"
    "AI 給你看似完美的清單——標題、作者、年份、DOI 一應俱全，但..."
)
st.markdown(
    "**真實情況**：在嵐海顧問的審稿經驗裡，AI 給的引用清單常出現以下狀況：\n"
    "- DOI 是真的，但**對應到完全不同的論文**\n"
    "- 標題是真的，但**作者名是 AI 編造**的（最危險、最難發現）\n"
    "- 期刊真的存在，但**該年份根本沒這篇**\n"
    "- 整段內容真的有，但**作者沒寫過那本書**\n\n"
    "**自救法**：每一筆引用都要回到 Google Scholar / Web of Science / 期刊官網**逐筆驗證**——"
    "不只看 DOI，要對照標題、作者、年份**三欄一致**才算過關。"
)

st.markdown("")

# === 陷阱 2 ===
st.markdown("### 陷阱 2 ｜ APA 格式中英混用")
st.warning(
    "**症狀**：同一段裡看到 `(Mishra & Koehler, 2006)` 和 "
    "`王明仁、李志偉（2018）` 並列。"
)
st.markdown(
    "**問題**：APA 規定**英文引用用半形括號 + 逗號**、"
    "**中文引用用全形括號 + 頓號**。"
    "混用看起來小事，但會讓審稿人覺得"
    "「這作者連格式都不熟，內容能信嗎？」\n\n"
    "**正確寫法**：\n"
    "- 英文：`(Mishra & Koehler, 2006)` 或 `Mishra 與 Koehler（2006）`\n"
    "- 中文：`（王明仁、李志偉，2018）` 或 `王明仁與李志偉（2018）`\n\n"
    "**自救法**：寫完一章後用 Find & Replace 統一掃過，"
    "或委託嵐海顧問做單章 APA 校對（NT$ 1,500）。"
)

st.markdown("")

# === 陷阱 3 ===
st.markdown("### 陷阱 3 ｜ 孤兒概念（理論只引一次）")
st.warning(
    "**症狀**：你在第二章引了 UTAUT 模型，但全章只出現這麼一次，"
    "後面就再也沒回頭呼應。"
)
st.markdown(
    "**問題**：這種「孤兒概念」會讓審稿人懷疑——"
    "**你到底懂不懂 UTAUT？是真的用，還是只是抓來填頁數？**\n\n"
    "**判準**：每個你引入的理論／模型，"
    "至少要在後續章節出現 3 次以上的對話：\n"
    "1. 第一次：定義與引入（誰提出、原意是什麼）\n"
    "2. 第二次：跟你的研究問題如何對接\n"
    "3. 第三次：在資料分析或討論章用它解讀資料\n\n"
    "**自救法**：寫完文獻探討後，列一張「理論清單」，"
    "凡是只出現一次的，**刪掉或補強**——不要中間態。"
)

st.markdown("")

# === 陷阱 4 ===
st.markdown("### 陷阱 4 ｜ 主框架錨點缺失")
st.warning(
    "**症狀**：你整本論文以 TPACK 為主框架，"
    "但**正文裡找不到 Mishra & Koehler (2006) 這個原始錨點**。"
)
st.markdown(
    "**問題**：審稿人的第一反應是"
    "「這作者真的讀過 TPACK 原始文獻嗎？"
    "還是只是引用其他人的二手詮釋？」\n\n"
    "**判準**：你論文最重要的 1–3 個核心框架，"
    "第一次出現時**必須引到原始作者的原始文獻**——"
    "不能只引綜述、不能只引中文翻譯版。\n\n"
    "**自救法**：把核心框架列出來，"
    "逐一檢查首次出現處是否引了 primary source。"
)

st.markdown("")

# === 陷阱 5 ===
st.markdown("### 陷阱 5 ｜ 段落主張無引用支撐")
st.warning(
    "**症狀**：你寫「近年來研究指出 AI 工具能有效提升教師備課效率」，"
    "但**這句話後面沒有任何引用**。"
)
st.markdown(
    "**問題**：在學術寫作裡，"
    "**任何「實質性主張」都必須在同句或前一句有引用**。"
    "沒有引用 = 你個人意見，不是學術論述。\n\n"
    "**判準**：拿一段你寫好的文字，逐句自問——\n"
    "- 這句是事實描述還是個人意見？\n"
    "- 如果是事實描述，引用在哪裡？\n"
    "- 如果是個人意見，是不是該移到討論章而非文獻探討？\n\n"
    "**自救法**：每寫完一段，逐句標註「來源」或「己論」，"
    "凡是「來源」卻沒引用的，補上引用或改寫成「己論」。"
)

